    /**
     * Display vehicle details and allow update
     */
    private void showVehicleDetails(Vehicle vehicle) {
        // Create input dialog to display details
        String details = "Vehicle Details:\n\n" +
                        "Vehicle ID: " + vehicle.getVehicleId() + "\n" +
                        "Make: " + vehicle.getMake() + "\n" +
                        "Model: " + vehicle.getModel() + "\n" +
                        "Year: " + vehicle.getYear() + "\n" +
                        "Registration Number: " + vehicle.getRegistrationNo() + "\n\n" +
                        "Owner Information:\n" +
                        "Owner ID: " + vehicle.getOwner().getOwnerId() + "\n" +
                        "Name: " + vehicle.getOwner().getFirstName() + " " + vehicle.getOwner().getLastName() + "\n" +
                        "Service Date: " + vehicle.getOwner().getServiceDate() + "\n\n" +
                        "Service Information:\n" +
                        "Service ID: " + vehicle.getSelectedService().getServiceId() + "\n" +
                        "Service Type: " + vehicle.getSelectedService().getServiceType() + "\n" +
                        "Cost: $" + vehicle.getSelectedService().getCost() + "\n" +
                        "Mechanic: " + vehicle.getSelectedService().getMechanicName() + "\n" +
                        "Service Duration: " + vehicle.getSelectedService().getServiceHours() + " hours";
        
        JOptionPane.showMessageDialog(this, details, "Vehicle Details", JOptionPane.INFORMATION_MESSAGE);
        
        // Ask if user wants to update information
        int update = JOptionPane.showConfirmDialog(this, 
            "Do you want to update vehicle information?", 
            "Update Confirmation", 
            JOptionPane.YES_NO_OPTION);
        
        if (update == JOptionPane.YES_OPTION) {
            updateVehicleInfo(vehicle);
        }
    }
    
    /**
     * Update vehicle information
     */
    private void updateVehicleInfo(Vehicle vehicle) {
        // Create input dialogs
        String newMake = JOptionPane.showInputDialog(this, "Enter new make:", vehicle.getMake());
        if (newMake == null || newMake.trim().isEmpty()) return;
        
        String newModel = JOptionPane.showInputDialog(this, "Enter new model:", vehicle.getModel());
        if (newModel == null || newModel.trim().isEmpty()) return;
        
        String newRegNo = JOptionPane.showInputDialog(this, "Enter new registration number:", vehicle.getRegistrationNo());
        if (newRegNo == null || newRegNo.trim().isEmpty()) return;
        
        // Update vehicle information
        vehicle.setMake(newMake.trim());
        vehicle.setModel(newModel.trim());
        vehicle.setRegistrationNo(newRegNo.trim());
        
        JOptionPane.showMessageDialog(this, 
            "Vehicle information updated successfully!", 
            "Success", 
            JOptionPane.INFORMATION_MESSAGE);
        
        // Refresh table
        loadAllVehicles();
    }




