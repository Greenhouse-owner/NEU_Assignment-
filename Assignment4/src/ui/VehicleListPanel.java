    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // Get selected row
        int selectedRow = tblManageVehicles.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Please select a vehicle first!", 
                "Notice", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Get vehicle ID
        String vehicleId = tableModel.getValueAt(selectedRow, 1).toString();
        Vehicle vehicle = vehicleDirectory.findVehicleById(vehicleId);
        
        if (vehicle != null) {
            // Display vehicle details and allow update
            showVehicleDetails(vehicle);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccountActionPerformed
        // Get selected row
        int selectedRow = tblManageVehicles.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Please select a vehicle first!", 
                "Notice", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Get vehicle ID
        String vehicleId = tableModel.getValueAt(selectedRow, 1).toString();
        
        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete vehicle " + vehicleId + "?", 
            "Confirm Deletion", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = vehicleDirectory.removeVehicle(vehicleId);
            if (success) {
                JOptionPane.showMessageDialog(this, 
                    "Vehicle deleted successfully!", 
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
                // Refresh table
                loadAllVehicles();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Deletion failed!", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteAccountActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchText = txtSearch.getText().trim();
        if (searchText.isEmpty()) {
            // If search box is empty, display all vehicles
            loadAllVehicles();
        } else {
            // Search by ID or make
            loadVehiclesBySearch(searchText);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

