    /**
     * Load all vehicles to table
     */
    private void loadAllVehicles() {
        tableModel.setRowCount(0);
        List<Vehicle> vehicles = vehicleDirectory.getAllVehicles();
        for (Vehicle vehicle : vehicles) {
            Object[] row = new Object[4];
            row[0] = vehicle.getOwner().getOwnerId();
            row[1] = vehicle.getVehicleId();
            row[2] = vehicle.getSelectedService().getServiceType();
            row[3] = vehicle.getSelectedService().getCost();
            tableModel.addRow(row);
        }
    }
    
    /**
     * Load vehicles by search criteria
     */
    private void loadVehiclesBySearch(String searchText) {
        tableModel.setRowCount(0);
        
        // Try to search by ID first
        Vehicle vehicle = vehicleDirectory.findVehicleById(searchText);
        if (vehicle != null) {
            Object[] row = new Object[4];
            row[0] = vehicle.getOwner().getOwnerId();
            row[1] = vehicle.getVehicleId();
            row[2] = vehicle.getSelectedService().getServiceType();
            row[3] = vehicle.getSelectedService().getCost();
            tableModel.addRow(row);
        } else {
            // Search by make
            List<Vehicle> vehicles = vehicleDirectory.findVehiclesByMake(searchText);
            if (vehicles.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "No matching vehicles found!", 
                    "Search Result", 
                    JOptionPane.INFORMATION_MESSAGE);
            } else {
                for (Vehicle v : vehicles) {
                    Object[] row = new Object[4];
                    row[0] = v.getOwner().getOwnerId();
                    row[1] = v.getVehicleId();
                    row[2] = v.getSelectedService().getServiceType();
                    row[3] = v.getSelectedService().getCost();
                    tableModel.addRow(row);
                }
            }
        }
    }

