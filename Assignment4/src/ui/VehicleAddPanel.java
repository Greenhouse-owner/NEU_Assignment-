    private void btnRegisterServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterServiceActionPerformed
        // Validate all fields are not empty
        if (txtOwnerID.getText().trim().isEmpty() || 
            txtOwnerFirstName.getText().trim().isEmpty() || 
            txtOwnerLastName.getText().trim().isEmpty() || 
            txtServiceDate.getText().trim().isEmpty() ||
            txtVehicleID.getText().trim().isEmpty() ||
            txtMake.getText().trim().isEmpty() ||
            txtModel.getText().trim().isEmpty() ||
            txtRegistration.getText().trim().isEmpty() ||
            txtServiceOpted.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "All fields are required and cannot be empty!", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            // Get and validate owner information
            int ownerId = Integer.parseInt(txtOwnerID.getText().trim());
            String firstName = txtOwnerFirstName.getText().trim();
            String lastName = txtOwnerLastName.getText().trim();
            
            // Parse service date (format: yyyy-MM-dd)
            LocalDate serviceDate;
            try {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                serviceDate = LocalDate.parse(txtServiceDate.getText().trim(), formatter);
            } catch (DateTimeParseException e) {
                JOptionPane.showMessageDialog(this, 
                    "Date format error! Please use format: yyyy-MM-dd\nExample: 2024-01-15", 
                    "Date Format Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Get and validate vehicle information
            String vehicleId = txtVehicleID.getText().trim();
            String make = txtMake.getText().trim();
            String model = txtModel.getText().trim();
            String registrationNo = txtRegistration.getText().trim();
            int serviceId = Integer.parseInt(txtServiceOpted.getText().trim());
            
            // Validate vehicle ID does not already exist
            if (vehicleDirectory.findVehicleById(vehicleId) != null) {
                JOptionPane.showMessageDialog(this, 
                    "Vehicle ID already exists! Please use a different ID.", 
                    "Validation Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Find service
            Service selectedService = serviceCatalog.findServiceById(serviceId);
            if (selectedService == null) {
                JOptionPane.showMessageDialog(this, 
                    "Service ID does not exist! Please create service first or use a valid service ID.", 
                    "Validation Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Create owner object
            Owner owner = new Owner(ownerId, firstName, lastName, serviceDate);
            
            // Create vehicle object (use current year as default)
            int currentYear = LocalDate.now().getYear();
            Vehicle vehicle = new Vehicle(vehicleId, make, model, currentYear, registrationNo, owner, selectedService);
            
            // Add to vehicle directory
            vehicleDirectory.addVehicle(vehicle);
            
            // Display success message
            JOptionPane.showMessageDialog(this, 
                "Vehicle and owner registered successfully!\n" +
                "Owner: " + owner.toString() + "\n" +
                "Vehicle: " + vehicle.toString() + "\n" +
                "Service: " + selectedService.toString(), 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // Clear form
            clearForm();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Please enter valid number format!\n" +
                "Owner ID: integer\n" +
                "Service ID: integer", 
                "Input Format Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterServiceActionPerformed
    
